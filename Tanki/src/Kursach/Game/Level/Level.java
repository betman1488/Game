    public static final int TILE_SCALE = 8; // размер тайла
    public static final int TILE_IN_GAME_SCALE = 2; //увеличение размера тайла
    public static final int WWW = TILE_SCALE * TILE_IN_GAME_SCALE;
    public static final int TILES_IN_WIDTH = Game.WIDTH / WWW;
    public static final int TILES_IN_HEIGHT = Game.HEIGHT / WWW;

    private Map<TyleType, Tile> tiles;
    private java.util.List<Point> grassCords;
    private java.util.List<Point> metalCords;

    private int[][] tileMap; //узнаем тайл в ширину и высоту

    public Level(TextureAtlas atlas) {
        tileMap = new int[TILES_IN_WIDTH][TILES_IN_HEIGHT];
        tiles = new HashMap<TyleType, Tile>();
        tiles.put(TyleType.BRICK, new Tile(atlas.cut(32 * TILE_SCALE, 0 * TILE_SCALE, TILE_SCALE, TILE_SCALE), TILE_IN_GAME_SCALE, TyleType.BRICK));
        tiles.put(TyleType.METAL, new Tile(atlas.cut(32 * TILE_SCALE, 2 * TILE_SCALE, TILE_SCALE, TILE_SCALE), TILE_IN_GAME_SCALE, TyleType.METAL));
        tiles.put(TyleType.WATER, new Tile(atlas.cut(32 * TILE_SCALE, 4 * TILE_SCALE, TILE_SCALE, TILE_SCALE), TILE_IN_GAME_SCALE, TyleType.WATER));
        tiles.put(TyleType.GRASS, new Tile(atlas.cut(34 * TILE_SCALE, 4 * TILE_SCALE, TILE_SCALE, TILE_SCALE), TILE_IN_GAME_SCALE, TyleType.GRASS));
        tiles.put(TyleType.ICE, new Tile(atlas.cut(36 * TILE_SCALE, 4 * TILE_SCALE, TILE_SCALE, TILE_SCALE), TILE_IN_GAME_SCALE, TyleType.ICE));
        tiles.put(TyleType.EMPTY, new Tile(atlas.cut(36 * TILE_SCALE, 6 * TILE_SCALE, TILE_SCALE, TILE_SCALE), TILE_IN_GAME_SCALE, TyleType.EMPTY));

        grassCords = new ArrayList<Point>();
        metalCords = new ArrayList<Point>();
        tileMap = lvl;
        for (int i = 0; i < tileMap.length; i++) {
            for (int j = 0; j < tileMap.length; j++){
                Tile tile = tiles.get(TyleType.fromNumeric(tileMap[i][j]));
                if (tile.type() == TyleType.GRASS)
                    grassCords.add(new Point(j * WWW, i * WWW));
            }
        }
        for (int i = 0; i < tileMap.length; i++) {
            for (int j = 0; j < tileMap.length; j++){
                Tile tile = tiles.get(TyleType.fromNumeric(tileMap[i][j]));
                if (tile.type() == TyleType.METAL)
                    metalCords.add(new Point(j * WWW, i * WWW));
            }
        }
    }

    public void update() {

    }

    public void render(Graphics2D g){
        for (int i = 0; i < tileMap.length; i++) {
            for (int j = 0; j < tileMap[i].length; j++) {
                Tile tile = tiles.get(TyleType.fromNumeric(tileMap[i][j]));
                if (tile.type() != TyleType.GRASS)
                    tile.render(g, j * WWW, i * WWW);
                if (tile.type() != TyleType.METAL)
                    tile.render(g, j * WWW, i * WWW);
            }
        }
    }


    public void renderGrass(Graphics2D g){
        for (Point p : grassCords) {
            tiles.get(TyleType.GRASS).render(g, p.x, p.y);
        }
    }

    public void renderMetal(Graphics2D g){
        for (Point p : metalCords) {
            tiles.get(TyleType.METAL).render(g, p.x, p.y);
        }
    }
